for i in range(10 ** 6):
    powers[i + 1] = powers[i] * power % mod


def op(x, y):
    a1, b1 = divmod(x, mod)
    a2, b2 = divmod(y, mod)
    return (a1 + a2) * mod + (b1 * powers[a2] + b2) % mod


def main():
    n, q = MII()
    s = I()
    seg = SegTree(op, 0, [mod + ord(x) - ord('a') + 1 for x in s])
    seg_rev = SegTree(op, 0, [mod + ord(x) - ord('a') + 1 for x in reversed(s)])
    for _ in range(q):
        query = LI()
        if query[0] == '1':
            idx, c = int(query[1]) - 1, ord(query[2]) - ord('a') + 1
            seg.set(idx, mod + c)
            seg_rev.set(n - 1 - idx, mod + c)
        else:
            l, r = int(query[1]) - 1, int(query[2]) - 1
            print('Yes' if seg.prod(l, r + 1) == seg_rev.prod(n - 1 - r, n - l) else 'No')
    return


